// Look up an abbreviation, falling back to the first two letters of the name so a
// shape is never left unlabelled if the geometry ships a name we have not mapped.
export function subAbbr(code, name) {
  const found = SUBDIVISION_ABBR[code]?.[name]
  if (found) return found
  const trimmed = (name || '').trim()
  return trimmed ? trimmed.slice(0, 2).toUpperCase() : ''
}

